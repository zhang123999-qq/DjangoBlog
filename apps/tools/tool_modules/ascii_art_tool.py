class AsciiArtTool(BaseTool):
    """е≠Чзђ¶зФїзФЯжИРеЩ®еЈ•еЕЈ"""
    name = "е≠Чзђ¶зФїзФЯжИРеЩ®"
    slug = "ascii-art"
    description = "е∞ЖжЦЗжЬђиљђжНҐдЄЇASCIIиЙЇжЬѓе≠Ч"
    icon = "fa fa-font"
    category = ToolCategory.GENERATE
    form_class = AsciiArtForm

    def handle(self, request, form):
        text = form.cleaned_data['text'].upper()
        style = form.cleaned_data['style']

        try:
            font = ASCII_FONTS.get(style, ASCII_FONTS['standard'])
            result = self._generate_ascii_art(text, font, style)

            return {
                'text': text,
                'style': style,
                'result': result,
                'copy_text': result,
            }

        except Exception as e:
            return {'error': f'зФЯжИРе§±иі•: {str(e)}'}

    def _generate_ascii_art(self, text, font, style):
        """зФЯжИРASCIIиЙЇжЬѓе≠Ч"""
        # иОЈеПЦжѓПи°МзЪДйЂШеЇ¶
        line_height = 5 if style != 'mini' else 2

        # жФґйЫЖжѓПдЄ™е≠Чзђ¶зЪДиЙЇжЬѓе≠Ч
        char_lines = []
        for char in text:
            if char in font:
                char_lines.append(font[char])
            elif char.upper() in font:
                char_lines.append(font[char.upper()])
            else:
                # жЬ™зЯ•е≠Чзђ¶зФ®з©Їж†Љдї£жЫњ
                if style == 'mini':
                    char_lines.append(['  ', '  '])
                else:
                    char_lines.append(['   '] * 5)

        # еРИеєґжЙАжЬЙе≠Чзђ¶
        result_lines = []
        for i in range(line_height):
            line_parts = []
            for char_line in char_lines:
                if i < len(char_line):
                    line_parts.append(char_line[i])
            result_lines.append(' '.join(line_parts))

        return '\n'.join(result_lines)
